import torch  # импортируем PyTorch для работы с тензорами и матрицами


# ============================
# Безопасный логарифм
# ============================
def safe_log(x):
    """
    Возвращает log(1 + |x|), чтобы:
    - избежать отрицательных аргументов для log
    - стабилизировать значения очень маленьких и больших величин
    """
    return torch.log1p(torch.abs(x))  # log1p(x) = log(1+x), работает лучше при малых x


def spectral_norm_estimate(A, iters=6):
    n = A.size(1)
    v = torch.ones(n, device=A.device, dtype=A.dtype)
    v = v / (torch.norm(v) + 1e-12)

    for _ in range(iters):
        v = A.T @ (A @ v)
        nv = torch.norm(v)
        if nv <= 1e-12:
            return torch.tensor(0.0, device=A.device, dtype=A.dtype)
        v = v / nv

    Av = A @ v
    return torch.norm(Av)


# ============================
# Извлечение признаков из матрицы
# ============================
def extract_features(A):
    """
    Быстрое извлечение признаков матрицы A.
    Сложность O(n^2), подходит даже для больших матриц (5000x5000).

    Возвращает:
        feats : torch.Tensor размерности [9]
    """

    A = A.float()  # приводим к float32 для экономии памяти и совместимости
    n = A.size(0)  # размер матрицы

    # ============================
    # Нормы матрицы
    # ============================
    norm1 = torch.linalg.norm(A, 1)  # норма 1 (максимум суммы по столбцам)
    norm_fro = torch.linalg.norm(A, 'fro')  # Фробениусова норма (sqrt(sum(A_ij^2)))

    # ============================
    # Трасса (сумма диагонали)
    # ============================
    trace = torch.trace(A)

    # ============================
    # Степень симметрии
    # ============================
    # если матрица полностью симметрична, A-A.T = 0
    symmetry = torch.norm(A - A.T) / (norm_fro + 1e-12)  # нормируем на Фробениусову норму

    # ============================
    # Разреженность
    # ============================
    # доля элементов, близких к нулю
    sparsity = (A.abs() < 1e-12).float().mean()

    # ============================
    # Максимальные и средние значения
    # ============================
    max_val = torch.max(torch.abs(A))  # максимальное по модулю

    # ============================
    # Формируем вектор признаков
    # ============================
    feats = torch.stack([
        torch.tensor(float(n), device=A.device),  # размер матрицы
        safe_log(norm1),  # log(1+||A||_1)
        safe_log(norm_fro),  # log(1+||A||_F)
        safe_log(max_val),  # log(1+максимум)
        torch.sign(trace) * torch.log1p(torch.abs(trace)),
        symmetry,  # симметрия
        sparsity  # разреженность
    ])

    return feats  # возвращаем тензор признаков